const SPRITE_DATA: Record<string, { pixels: string[][]; width: number; height: number }> = {
  ...INTERNAL_SPRITES,
  ...ENEMY_SPRITES,
};

// Canvas 渲染缓存：key = "name|size"，避免重复渲染
const spriteCache = new Map<string, string>();

/** 用 Canvas 将像素数据渲染为 data URL（替代 box-shadow，兼容小游戏 Canvas 环境） */
const renderToDataURL = (pixels: string[][], w: number, h: number, pixelSize: number): string => {
  const canvas = document.createElement('canvas');
  canvas.width = w * pixelSize;
  canvas.height = h * pixelSize;
  const ctx = canvas.getContext('2d')!;
  for (let row = 0; row < pixels.length; row++) {
    for (let col = 0; col < pixels[row].length; col++) {
      const color = pixels[row][col];
      if (color) {
        ctx.fillStyle = color;
        ctx.fillRect(col * pixelSize, row * pixelSize, pixelSize, pixelSize);
      }
    }
  }
  return canvas.toDataURL();
};

const FALLBACK_PIXELS = [
  ['', '#c8c8d0', '#c8c8d0', '#c8c8d0', '#c8c8d0', ''],
  ['#c8c8d0', '#c8c8d0', '#c8c8d0', '#c8c8d0', '#c8c8d0', '#c8c8d0'],
  ['#c8c8d0', '#1a1a2e', '#c8c8d0', '#c8c8d0', '#1a1a2e', '#c8c8d0'],
  ['#c8c8d0', '#c8c8d0', '#c8c8d0', '#c8c8d0', '#c8c8d0', '#c8c8d0'],
  ['', '#c8c8d0', '#1a1a2e', '#1a1a2e', '#c8c8d0', ''],
  ['', '', '#c8c8d0', '#c8c8d0', '', ''],
];

export const PixelSprite: React.FC<PixelSpriteProps> = ({ name, size = 4, className = '' }) => {
  const spriteData = SPRITE_DATA[name];

  const pixels = spriteData?.pixels ?? FALLBACK_PIXELS;
  const w = spriteData?.width ?? 6;
  const h = spriteData?.height ?? 6;
  const totalWidth = w * size;
  const totalHeight = h * size;

  const cacheKey = `${name}|${size}`;
  if (!spriteCache.has(cacheKey)) {
    spriteCache.set(cacheKey, renderToDataURL(pixels, w, h, size));
  }
  const src = spriteCache.get(cacheKey)!;

  return (
    <img
      src={src}
      className={className}
      width={totalWidth}
      height={totalHeight}
      style={{ imageRendering: 'pixelated', display: 'block' }}
      alt={name}
    />
  );
};

// 获取精灵是否存在（用于判断是否fallback到emoji）
export const hasSpriteData = (name: string): boolean => {
  return name in SPRITE_DATA;
};

export default PixelSprite;
